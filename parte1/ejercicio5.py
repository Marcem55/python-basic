x = 20
y = 5

### ARITMETIC OPERATORS

# Sum (+)
print("Sum:", x + y)

# Substract (-)
print("Substract:", x - y)

# Product (*)
print("Product:", x * y)

# Division (/)
print("Division:", x / y)

# Ints of the division (//)
print("Ints of the division:", x // y)

# Exponencial (**)
print("Exponencial:", x ** y)


### LOGICAL OPERATORS

# Greather than (>)
print("Greather than:", x > y)

# Less than (<)
print("Less than:", x < y)

# Equal to (==)
print("Equal to:", x == y)

# Not equal to (!=)
print("Not equal to:", x != y)

# Greather or equal to (>=)
print("Greather or equal to:", x >= y)

# Less or equal to (<=)
print("Less or equal to:", x <= y)
